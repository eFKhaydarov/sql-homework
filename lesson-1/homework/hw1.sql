1. Vazifa
• Ma’lumot (data): Har qanday raqam, matn yoki boshqa shakldagi axborot.
• Ma’lumotlar bazasi (database): Ma’lumotlarni saqlash va boshqarish uchun tashkil etilgan tizim.
• Relatsion ma’lumotlar bazasi (relational database): Jadvallar orqali bog‘langan ma’lumotlar bazasi.
• Jadval (table): Ma’lumotlar satr (row) va ustunlar (column) shaklida saqlanadigan struktura.

2. Vazifa
1) Ma’lumotlarni xavfsiz saqlash va boshqarish.
2) Katta hajmdagi ma’lumotlar bilan ishlash imkoniyati.
3) Yaxshi ishlov beruvchi so‘rov tizimi (query engine).
4) Zaxira nusxalash (backup) va tiklash (restore) funksiyalari.
5) Turli dasturlar bilan integratsiya qilish imkoniyati (Excel, Power BI, .NET va h.k.).

3. Vazifa
1) Windows Authentication – Windows tizimi orqali kirish.
2) SQL Server Authentication – Foydalanuvchi nomi va parol orqali kirish.

