6. Vazifa
• SQL Server – Ma’lumotlar bazasini boshqaruvchi server dasturi.
• SSMS (SQL Server Management Studio) – SQL Server bilan ishlash uchun grafik interfeysga ega dastur.
• SQL (Structured Query Language) – Ma’lumotlar bilan ishlash uchun dasturlash tili.

